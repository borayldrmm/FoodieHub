    message: String,
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier
            .fillMaxWidth()
            .padding(32.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        Icon(
            imageVector = Icons.Default.Receipt,
            contentDescription = null,
            modifier = Modifier.size(80.dp),
            tint = MaterialTheme.colorScheme.onSurfaceVariant.copy(alpha = 0.4f)
        )

        Spacer(modifier = Modifier.height(16.dp))

        Text(
            text = message,
            style = MaterialTheme.typography.titleMedium,
            color = MaterialTheme.colorScheme.onSurfaceVariant
        )
    }
}

/**
 * Formats timestamp to relative time
 * Examples: "2 saat önce", "Dün", "3 gün önce"
 *    /-> "2 hour ago", "yesterday", "3 day ago"
 */
fun formatRelativeTime(timestamp: Long): String {
    val now = System.currentTimeMillis()
    val diff = now - timestamp

    val seconds = diff / 1000
    val minutes = seconds / 60
    val hours = minutes / 60
    val days = hours / 24

    return when {
        minutes < 1 -> "Az önce"
        minutes < 60 -> "$minutes dakika önce"
        hours < 24 -> "$hours saat önce"
        days < 1L -> "Dün"
        days < 7 -> "$days gün önce"
        else -> {
            val dateFormat = SimpleDateFormat("dd MM yyyy", Locale("tr"))
            dateFormat.format(Date(timestamp))
        }
    }
}